package Number;
//比较运算符
public class Compare {
    public static void main(String[] args){
        int number1 = 4;
        int number2 = 5;
        System.out.println("比较大于运算返回值："+(number1>number2));
        System.out.println("比较小于运算返回值："+(number1<number2));
        System.out.println("比较两值是否相等："+(number1==number2));
        System.out.println("比较两值是否不相等："+(number1!=number2));
    }
}
